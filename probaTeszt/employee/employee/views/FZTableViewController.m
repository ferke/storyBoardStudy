- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"EmployeeCell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    
    UILabel* nameLabel = (UILabel*)[cell viewWithTag:100];
    UILabel* postLabel = (UILabel*)[cell viewWithTag:101];
    UIImageView* portraitImage = (UIImageView*)[cell viewWithTag:102];
    
    FZEmployee* employee = [_DisplayElements objectAtIndex:indexPath.row];
    
    nameLabel.text = employee.Name;
    postLabel.text = employee.Post;
    [portraitImage setImage:employee.Portrait];
    
    return cell;
}

